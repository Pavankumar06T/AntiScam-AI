"""Evidence & Reporting Agent.

Turns a confirmed-fraud session into a structured complaint draft.

The value here is narrow but real. A victim files a complaint hours later, from
memory, while distressed and often ashamed — and the details that actually let
police act (the exact UPI ID, the account number, the precise words used, the
timestamps) are exactly the details that evaporate under stress. We captured them
while the call was happening. This agent just assembles them.

The packet is **a draft**, not a filing. It is not submitted anywhere. It exists to
be reviewed by the person, then pasted into cybercrime.gov.in or handed to an
officer at a station.

Construction is deliberately mostly deterministic: identifiers, quotes, timestamps
and cluster references are assembled from data we already hold, not regenerated by
a model. The LLM writes only the prose summary. An invented account number in a
police complaint would be actively harmful, so the model is never given the chance
to produce one.
"""

from __future__ import annotations

import json
import logging
from datetime import datetime, timezone

from app.config import get_settings, resolve_model_name
from app.models.advisory_schemas import ComplaintEvidence, ComplaintPacket
from app.models.graph_schemas import GraphMatch
from app.models.schemas import ClassifyResponse, RedFlagCategory, ScamType

logger = logging.getLogger(__name__)


COMPLAINT_DISCLAIMER = (
    "This is an AUTO-GENERATED DRAFT produced by AntiScam AI, a prototype system. "
    "It has NOT been filed with any authority and is NOT a legal document or legal "
    "advice. Statutory provisions listed are illustrative and may not be the ones "
    "actually applied. Review every detail for accuracy before submitting. To file "
    "for real, call 1930 or visit cybercrime.gov.in."
)

REPORTING_SYSTEM_PROMPT = """\
You are the Reporting Agent of AntiScam AI. You write the narrative summary of a \
cyber-fraud complaint that a victim will submit to Indian police.

Write for an investigating officer: factual, chronological, unemotional. This is a \
legal-adjacent document, not a story.

Rules:
- 3-6 sentences. Chronological: how contact was made, what was claimed, what \
pressure was applied, what was demanded.
- ONLY state facts present in the evidence given to you. Never invent an account \
number, phone number, name, amount, time, or case reference. If a detail is not \
provided, omit it — do not approximate it.
- Refer to the person as "the complainant" and the fraudster as "the caller".
- No adjectives of outrage. "The caller threatened arrest" — not "the caller \
cruelly terrorised the complainant".
- Do not assert legal conclusions ("this constitutes cheating under section X"). \
Describe conduct; let the officer classify it.

Respond with a single JSON object ONLY:
{"incident_summary": "<the narrative>"}
"""


