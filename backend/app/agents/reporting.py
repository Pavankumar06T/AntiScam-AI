def _now() -> str:
    return datetime.now(timezone.utc).isoformat(timespec="seconds")


def _complaint_id(session_id: str) -> str:
    stamp = datetime.now(timezone.utc).strftime("%Y%m%d")
    suffix = "".join(c for c in session_id if c.isalnum())[-6:].upper() or "000000"
    return f"ASAI-{stamp}-{suffix}"


def _fallback_summary(detection: ClassifyResponse, match: GraphMatch | None) -> str:
    flags = ", ".join(
        f.category.value.replace("_", " ") for f in detection.red_flags[:5]
    )
    parts = [
        f"The complainant received a communication assessed by AntiScam AI as "
        f"{detection.scam_type.value.replace('_', ' ')} with a risk score of "
        f"{detection.scam_probability}/100."
    ]
    if flags:
        parts.append(f"The following coercion patterns were identified: {flags}.")
    if detection.extracted_entities.claimed_departments:
        parts.append(
            "The caller claimed to represent: "
            f"{', '.join(detection.extracted_entities.claimed_departments)}."
        )
    if match and match.is_repeat_scammer:
        parts.append(
            f"Identifiers in this incident match {match.total_victims_in_cluster} "
            f"previously recorded session(s) (cluster {match.cluster_id})."
        )
    return " ".join(parts)


def _generate_summary(
    detection: ClassifyResponse, match: GraphMatch | None
) -> tuple[str, bool]:
    """Returns (summary, degraded)."""
    settings = get_settings()
    if not settings.groq_configured:
        return _fallback_summary(detection, match), True

    evidence_block = "\n".join(
        f'- [{f.timestamp or "no timestamp"}] {f.category.value}: "{f.quote}"'
        for f in detection.red_flags[:10]
    )
    e = detection.extracted_entities
    identifiers = "\n".join(
        f"- {label}: {', '.join(vals)}"
        for label, vals in (
            ("Phone numbers", e.phone_numbers),
            ("UPI IDs", e.upi_ids),
            ("Bank accounts", e.bank_accounts),
            ("Claimed identity", e.claimed_names),
            ("Claimed agency", e.claimed_departments),
            ("Case references cited", e.case_numbers),
            ("Amounts mentioned", e.amounts_mentioned),
        )
        if vals
    )
    network = ""
    if match and match.is_repeat_scammer:
        network = (
            f"\n\nFRAUD NETWORK: identifiers match {match.total_victims_in_cluster} "
            f"prior session(s), cluster {match.cluster_id}."
        )

    prompt = f"""Write the incident summary for this complaint.

ASSESSMENT: {detection.scam_type.value}, risk {detection.scam_probability}/100
ANALYSIS: {detection.reasoning}

QUOTED EVIDENCE:
{evidence_block or "(none)"}

IDENTIFIERS CAPTURED:
{identifiers or "(none)"}{network}

Return JSON with the incident_summary."""

    try:
        from groq import Groq

        client = Groq(
            api_key=settings.groq_api_key,
            timeout=settings.request_timeout,
            max_retries=0,
        )
        completion = client.chat.completions.create(
            model=resolve_model_name(),
            messages=[
                {"role": "system", "content": REPORTING_SYSTEM_PROMPT},
                {"role": "user", "content": prompt},
            ],
            temperature=0.2,
            max_completion_tokens=700,
            response_format={"type": "json_object"},
            seed=13,
        )
        payload = json.loads(completion.choices[0].message.content or "")
        summary = str(payload.get("incident_summary", "")).strip()
        if not summary:
            raise ValueError("empty incident_summary")
        return summary, False
    except Exception as exc:
        logger.warning(
            "Reporting summary generation failed (%s); using deterministic fallback.",
            type(exc).__name__,
        )
        return _fallback_summary(detection, match), True


def build_complaint(
    session_id: str,
    detection: ClassifyResponse,
    match: GraphMatch | None = None,
) -> ComplaintPacket:
    """Assemble an NCRB-style complaint draft from a confirmed-fraud session."""
    summary, degraded = _generate_summary(detection, match)

    evidence = [
        ComplaintEvidence(
            timestamp=f.timestamp,
            turn_index=f.turn_index,
            speaker="caller",
            quote=f.quote,
            significance=_SIGNIFICANCE.get(
                f.category, f.category.value.replace("_", " ").capitalize()
            ),
        )
        for f in detection.red_flags
    ]

    provisions = _PROVISIONS_BY_TYPE.get(detection.scam_type, []) + _PROVISIONS_COMMON
    # Preserve order while removing duplicates.
    provisions = list(dict.fromkeys(provisions))

    e = detection.extracted_entities
    network_note = None
    if match and match.is_repeat_scammer:
        network_note = (
            f"{match.summary} This indicates an organised operation rather than an "
            f"isolated incident, and the linked sessions may support a consolidated "
            f"investigation."
        )

    return ComplaintPacket(
        complaint_id=_complaint_id(session_id),
        generated_at=_now(),
        session_id=session_id,
        incident_type=detection.scam_type.value.replace("_", " ").title(),
        incident_summary=summary,
        scam_probability=detection.scam_probability,
        estimated_loss=e.amounts_mentioned[0] if e.amounts_mentioned else None,
        suspect_phone_numbers=e.phone_numbers,
        suspect_upi_ids=e.upi_ids,
        suspect_bank_accounts=e.bank_accounts,
        suspect_claimed_identity=e.claimed_names,
        suspect_claimed_agency=e.claimed_departments,
        fake_case_references=e.case_numbers,
        suspect_urls=e.urls,
        evidence=evidence,
        linked_cluster_id=match.cluster_id if match and match.is_repeat_scammer else None,
        linked_session_count=match.total_victims_in_cluster if match and match.is_repeat_scammer else 0,
        linked_sessions=[s.session_id for s in match.linked_sessions] if match else [],
        network_note=network_note,
        applicable_provisions=provisions,
        recommended_channels=list(_CHANNELS),
        disclaimer=COMPLAINT_DISCLAIMER,
        degraded=degraded,
    )
